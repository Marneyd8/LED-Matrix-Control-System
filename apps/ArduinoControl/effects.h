#ifndef EFFECTS_H
#define EFFECTS_H

// Function to create a spiral effect on the LED matrix
// This function will generate a spiral pattern on the LED display.
void spiralEffect();

// Function to create a wave effect on the LED matrix
// This function will simulate a wave moving across the LED display.
void waveEffect();

// Function to create a random effect on the LED matrix
// This function will generate random colors on the LEDs in a random pattern.
void randomEffect();

// Function to create a fire effect on the LED matrix
// This function simulates a fire-like effect, often with red, orange, and yellow colors.
void fireEffect();

// Function to create a water effect on the LED matrix
// This function simulates a water ripple effect, often with shades of blue and white.
void waterEffect();

#endif
